function a = formatConvert(x)
%% 数值数组转字符串格式化函数
%
% 该函数将数值数组转换为以逗号分隔的字符串格式，
% 主要用于将节点特征数据转换为CSV文件可接受的字符串格式。
%
% 输入参数:
%   x - 数值数组，包含要转换的数据
%
% 输出参数:
%   a - 转换后的字符串，以逗号分隔各元素
%

% 初始化空字符串
a = '';

% 遍历数组中的每个元素，将其转换为字符串并用逗号连接
for i = 1:length(x)
    a = [a, num2str(x(i)), ','];
end

% 删除最后一个逗号（避免末尾多余的逗号）
a(end) = '';